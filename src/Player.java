public class Player {
    public Player(){
    }
    private int score;
    private int skipQ;
    private int solveQ;

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public int getSkipQ() {
        return skipQ;
    }

    public void setSkipQ(int skipQ) {
        this.skipQ = skipQ;
    }

    public int getSolveQ() {
        return solveQ;
    }

    public void setSolveQ(int solveQ) {
        this.solveQ = solveQ;
    }
}
